#list/array/collection all of them are same(simple term)
#index   0  1   2  3  4  5  6
numbers=[34,6,712,12,45,59,46]
#index  -7  -6 -5 -4 -3 -2 -1 (from reverse direction)


#list( start :end)
print(numbers[2:6]) #2 hote suru kore 6 er ag porjonto
print(numbers[1:7])

##list (start:end:step)
print(numbers[1:7:2])
print(numbers[6:2:-2]) #ulta dik diye jabo
print(numbers[4:])
print(numbers[:5])
print(numbers[::-1])#reverse a array
print(numbers[:])#shortcut to copy a list